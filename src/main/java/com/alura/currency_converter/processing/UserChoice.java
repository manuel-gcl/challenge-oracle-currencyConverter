package com.alura.currency_converter.processing;

/**
 * Defines the options available to the user in the Currency Converter
 * application. Options: - ONE_TO_ONE: Represents a single currency conversion
 * operation. - ONE_TO_MANY: Represents a multi-currency conversion operation. -
 * VIEW_HISTORY: Allows the user to view the history of currency conversion
 * operations. - EXIT: Exit the application.
 */

public enum UserChoice {
	ONE_TO_ONE, ONE_TO_MANY, VIEW_HISTORY, EXIT
}